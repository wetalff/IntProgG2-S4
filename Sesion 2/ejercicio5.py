#Algoritmo que calcule el porcentaje de un número dado. Ejemplo: ¿qué es el 15% de 200?

cantidad = float(input("Dime la cantidad a manipular: "))
porcentaje = float(input("Dime el porcentaje que le quieres sacar a la cantidad mencionada: "))
porcentaje_aplicado = (porcentaje * cantidad) /100

print("El porcentaje mencionado aplicado a la cantidad proporcionada da un resultado de:", porcentaje_aplicado) 

 
